-- CRIANDO BANCO DE DADOS
IF NOT EXISTS (SELECT 1 FROM SYS.databases WHERE name = 'BD2')
BEGIN 
 CREATE DATABASE BD2
END
GO

-- CRIANDO TABELA DE PRODUTOS
CREATE TABLE BD2.DBO.Produtos (
	[CodProduto] [int] NOT NULL,
	[Descricao] [varchar](50) NULL,
	[CodFabricante] [int] NOT NULL,
	[CodEAN] [varchar](14) NULL,
	[NumRegMS] [varchar](20) NULL,
	[CodNCM] [varchar](12) NULL,
	[PrcFabricante] [numeric](18, 4) NULL,
	[PrcVenda] [numeric](18, 4) NULL,
 CONSTRAINT [PK_Produtos] PRIMARY KEY NONCLUSTERED 
(
	[CodProduto] ASC
)
)
GO

-- CRIANDO TABELA DE FABRICANTES
CREATE TABLE BD2.DBO.Fabricantes (
	[CodFabricante] [int] NOT NULL,
	[Descricao] [varchar](50) NULL,
 CONSTRAINT [PK_Fabricante] PRIMARY KEY NONCLUSTERED 
(
	[CodFabricante] ASC
)
)
GO

-- CRIANDO FOREIGN KEY PRODUTOS
ALTER TABLE BD2.DBO.Produtos  WITH NOCHECK ADD  CONSTRAINT [FK_Produto_Fabricante] FOREIGN KEY([CodFabricante])
REFERENCES BD2.DBO.Fabricantes ([CodFabricante])
NOT FOR REPLICATION 
GO

ALTER TABLE Produtos CHECK CONSTRAINT [FK_Produto_Fabricante]
GO

